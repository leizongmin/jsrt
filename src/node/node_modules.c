static JSValue js_node_http2_server_request_ctor(JSContext* ctx, JSValueConst new_target, int argc,
                                                 JSValueConst* argv) {
  return JS_UNDEFINED;
}

JSValue JSRT_InitNodeHttp2(JSContext* ctx) {
  JSValue http2_obj = JS_NewObject(ctx);
  JSValue ctor =
      JS_NewCFunction2(ctx, js_node_http2_server_request_ctor, "Http2ServerRequest", 1, JS_CFUNC_constructor, 0);

  // Create and set prototype for instanceof checks
  JSValue prototype = JS_NewObject(ctx);
  JS_SetPropertyStr(ctx, ctor, "prototype", prototype);

  JS_SetPropertyStr(ctx, http2_obj, "Http2ServerRequest", ctor);
  return http2_obj;
}

int js_node_http2_init(JSContext* ctx, JSModuleDef* m) {
  JSValue http2_obj = JSRT_InitNodeHttp2(ctx);

  JSValue ctor = JS_GetPropertyStr(ctx, http2_obj, "Http2ServerRequest");
  JS_SetModuleExport(ctx, m, "Http2ServerRequest", JS_DupValue(ctx, ctor));
  JS_FreeValue(ctx, ctor);

  JS_SetModuleExport(ctx, m, "default", JS_DupValue(ctx, http2_obj));
  JS_FreeValue(ctx, http2_obj);
  return 0;
}

